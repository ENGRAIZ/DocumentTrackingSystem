    public void downloadFile(int fileID) {
        if (isDownloading) {
        return; // If already downloading, do nothing
        }
        
        isDownloading = true;
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setDialogTitle("Specify a file to save");

        int userSelection = fileChooser.showSaveDialog(null);

        if (userSelection == JFileChooser.APPROVE_OPTION) {
            try {
                File selectedFile = fileChooser.getSelectedFile();

                Class.forName("com.mysql.cj.jdbc.Driver");
                cn = DriverManager.getConnection("jdbc:mysql://localhost:3306/docutable", "root", "");
                String sql = "SELECT file, filename, file_extension FROM "+table+" WHERE ID = ?";
                PreparedStatement pstmt = cn.prepareStatement(sql);
                pstmt.setInt(1, fileID);
                ResultSet rs = pstmt.executeQuery();

                if (rs.next()) {
                    Blob fileBlob = rs.getBlob("file");
                    String fileName = rs.getString("filename");
                    String fileExtension = rs.getString("file_extension");

                    InputStream is = fileBlob.getBinaryStream();
                    FileOutputStream fos = new FileOutputStream(selectedFile.getPath() + "." + fileExtension); // Add the file extension
                    byte[] buffer = new byte[1024];
                    int bytesRead;
                    while ((bytesRead = is.read(buffer)) != -1) {
                        fos.write(buffer, 0, bytesRead);
                    }
                    fos.close();
                    is.close();

                    JOptionPane.showMessageDialog(null, "File downloaded successfully.");
                } else {
                    JOptionPane.showMessageDialog(null, "File not found.");
                }

                rs.close();
                pstmt.close();
                cn.close();
            } catch (SQLException | IOException | ClassNotFoundException e) {
                e.printStackTrace();
            }
            isDownloading = false;
        }
    }





